        <h1>Información sobre el Centro Educativo "Rosario Ocampo de Iribarren"</h1>

        <p>El Centro Educativo "Rosario Ocampo de Iribarren", anteriormente conocido como Escuela N° 406, es una institución educativa pública ubicada en Nonogasta, Departamento Chilecito, provincia de La Rioja, Argentina. Se encuentra en el predio del Hipódromo, sobre la calle 21 de Septiembre s/n°, código postal F5372.</p>

        <p>La institución ofrece una educación integral que abarca desde el nivel inicial hasta el nivel secundario, incluyendo modalidades comunes y técnicas. Su misión es formar estudiantes con sólidos conocimientos y valores, preparados para enfrentar los desafíos del mundo actual.</p>

        <p>Gracias a gestiones provinciales y nacionales, se está construyendo un nuevo edificio educativo que permitirá mejorar las condiciones de enseñanza y aprendizaje, brindando a la comunidad nonogasteña un espacio moderno y adecuado para la educación.</p>

        <p>En el ámbito de la salud mental, el centro educativo colabora activamente con el Hospital Eleázar Herrera Motta de Chilecito. Recientemente, se llevaron a cabo talleres de prevención del suicidio, dirigidos a estudiantes y docentes, con el objetivo de promover el bienestar emocional y prevenir situaciones de riesgo.</p>

